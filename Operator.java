// Java Relational Operators
public class Operator {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        // Relational operators
        System.out.println("a == b: " + (a == b)); // false  meaning a is not equal to b
        System.out.println("a != b: " + (a != b)); // true meaning a is not equal to b
        System.out.println("a > b: " + (a > b));   // false meaning a is not greater than b
        System.out.println("a < b: " + (a < b));   // true meaning a is less than b
        System.out.println("a >= b: " + (a >= b)); // false meaning a is not greater than or equal to b
        System.out.println("a <= b: " + (a <= b)); // true meaning a is less than or equal to b
    }
}
